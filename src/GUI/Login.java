    private void btn_selecionarProdutosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_selecionarProdutosActionPerformed
        int i = TabelaBuscaProdutos.getSelectedRow();
        if (i== -1){
            JOptionPane.showMessageDialog(null, "SELECIONE UM PRODUTO");
        }else {
            txtIdcliente.setText(String.valueOf(TabelaBuscaProdutos.getValueAt(i, 0)));
            txtProduto_cliente.setText(String.valueOf(TabelaBuscaProdutos.getValueAt(i, 1)));
            txt_Corcliente.setText(String.valueOf(TabelaBuscaProdutos.getValueAt(i, 2)));
            txt_Anocliente.setText(String.valueOf(TabelaBuscaProdutos.getValueAt(i, 3)));
            txt_valorcliente.setText(String.valueOf(TabelaBuscaProdutos.getValueAt(i, 4)));
        }
        jd_BuscarProdutos.dispose();
    }//GEN-LAST:event_btn_selecionarProdutosActionPerformed

