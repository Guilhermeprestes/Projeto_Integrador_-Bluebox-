    public void logar(){
        
        this.tip = cbTipe.getSelectedIndex();
        
        if (tip == 0){ //login de adm
            
            String sql = "Select * from usuario_tb where usuario=? and senha=?";
            try {
                conn = conexao.getConexion();
                pst = conn.prepareStatement(sql);
                pst.setString(1, txtusuario.getText());
                pst.setString(2, txtSenha.getText());
                
                rs = pst.executeQuery();
                
                if(rs.next()){
                    homeAdm adm = new homeAdm();
                    adm.setVisible(true);
                    
                    
                }else{
                    JOptionPane.showMessageDialog(null, "USUÁRIO OU SENHA INVÁLIDOS(S)!");
                }
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }//fim do try
            
        }
        else if(tip == 1){ //login de gerente
                String sql = "Select * from gerente_tb where email=? and senha=?";
            try {
                conn = conexao.getConexion();
                pst = conn.prepareStatement(sql);
                pst.setString(1, txtusuario.getText());
                pst.setString(2, txtSenha.getText());
                
                rs = pst.executeQuery();
                
                if(rs.next()){
                    homeGerente ger = new homeGerente();
                    ger.setVisible(true);
                }else{
                    JOptionPane.showMessageDialog(null, "USUÁRIO OU SENHA INVÁLIDOS(S)!");
                }
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }//fim do try
                }
        else if(tip == 2){ // login de cliente
                String sql = "Select * from cliente_tb where email_cliente=? and senha_cliente=?";
            try {
                conn = conexao.getConexion();
                pst = conn.prepareStatement(sql);
                pst.setString(1, txtusuario.getText());
                pst.setString(2, txtSenha.getText());
                
                rs = pst.executeQuery();
                
                if(rs.next()){
                    /***********************************/
                    Login.idUsuario = rs.getString("id_cliente");
                    homeCliente client = new homeCliente(idUsuario);
                    client.setVisible(true);
                    client.setLocation(300, 200);
                    
                    /*****************************************/
                }else{
                    JOptionPane.showMessageDialog(null, "USUÁRIO OU SENHA INVÁLIDOS(S)!");
                }
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }//fim do try
                }
        else{
            JOptionPane.showMessageDialog(null, "selecione uma categoria");
        }
            
              
    }//fim metodo logar
    
    
   
    
    
