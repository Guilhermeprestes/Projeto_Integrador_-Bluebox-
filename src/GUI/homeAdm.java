        String Tipe = "gerente";
        
        String InsertSql = "INSERT INTO gerente_tb (Nome,cpf,telefone,email,senha,tipe)"
           + " VALUES(?,?,?,?,?,?)";
        
    Connection conn = null;
    PreparedStatement pstm = null ;
     
  
        try {
            conn = conexao.getConexion();
            pstm = conn.prepareStatement(InsertSql);
            pstm.setString(1, txtNome_gerente.getText());
            pstm.setDouble(2,Integer.parseInt(txtCPF_gerente.getText()) );
            pstm.setInt(3,Integer.parseInt(txtTel_gerente.getText()) );
            pstm.setString(4, txtEmail_gerente.getText());
            pstm.setString(5, txtSenha_gerente.getText());
            pstm.setString(6, Tipe);
           
            pstm.execute();
             JOptionPane.showMessageDialog(null, "GERENTE CADASTRADO COM SUCESSO!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "ERRO SQL:\n" + e);
        }
        CadGerente.dispose();
