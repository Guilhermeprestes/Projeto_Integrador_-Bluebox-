    private void btnSalvar_clienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvar_clienteActionPerformed
        String tipe = "cliente";
        String InsertSql = "INSERT INTO cliente_tb (nome_cliente,cpf_cliente,telefone_cliente,email_cliente,"+
               "senha_cliente,idproduto_cliente,cor_cliente,ano_cliente,valor_cliente,marca_cliente,tipe)"
           + " VALUES(?,?,?,?,?,?,?,?,?,?,?)";
        
    Connection conn = null;
    PreparedStatement pstm = null ;
     
  
        try {
            conn = conexao.getConexion();
            pstm = conn.prepareStatement(InsertSql);
            pstm.setString(1, txtNome_Cliente.getText());
            pstm.setInt(2,Integer.parseInt(txtCPF_Cliente.getText()));
            pstm.setInt(3,Integer.parseInt(txtTel_Cliente.getText()));
            pstm.setString(4, txtEmail_Cliente.getText());
            pstm.setString(5, txtSenha_Cliente.getText());
            pstm.setInt(6,Integer.parseInt(txtIdcliente.getText()));
            pstm.setString(7, txt_Corcliente.getText());
            pstm.setInt(8,Integer.parseInt(txt_Anocliente.getText()));            
            pstm.setDouble(9,Double.parseDouble(txt_valorcliente.getText()));
            pstm.setString(10, txtProduto_cliente.getText());
            pstm.setString(11,tipe);
           
           
            pstm.execute();
             JOptionPane.showMessageDialog(null, "CLIENTE CADASTRADO COM SUCESSO!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "ERRO SQL:\n" + e);
        }
        CadCliente.dispose();
    }//GEN-LAST:event_btnSalvar_clienteActionPerformed

